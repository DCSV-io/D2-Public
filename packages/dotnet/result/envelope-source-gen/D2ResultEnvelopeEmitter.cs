namespace DcsvIo.D2.Result.Envelope.SourceGen;

using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Text;
using System.Text.RegularExpressions;
using DcsvIo.D2.SourceGen;
using DcsvIo.D2.SourceGen.Polyfills;

/// <summary>
/// Pure logic for emitting the <c>D2ResultEnvelopeFieldNames</c> static
/// class carrying JSON property-name constants for the D2Result Shape B
/// wire envelope. Stateless and unit-testable in isolation. The
/// <c>D2Result</c> properties carry
/// <c>[JsonPropertyName(D2ResultEnvelopeFieldNames.SUCCESS)]</c> (etc.)
/// attributes referencing these constants — single source of truth for
/// the wire-key strings. The companion TS emitter
/// <c>tools/ts-codegen/src/d2result-envelope-emit.ts</c> reads the same
/// spec and emits an identical <c>D2ResultEnvelopeFieldNames</c> catalog
/// into <c>@dcsv-io/d2-result</c>; cross-language wire drift on these field
/// names is structurally impossible.
/// </summary>
internal static class D2ResultEnvelopeEmitter
{
    /// <summary>The namespace the emitted artifact lives in.</summary>
    public const string ROOT_NAMESPACE = "DcsvIo.D2.Result";

    /// <summary>The emitted catalog class name.</summary>
    public const string CLASS_NAME = "D2ResultEnvelopeFieldNames";

    private static readonly Regex sr_constNameRegex = new(
        "^[A-Z][A-Z0-9_]*$",
        RegexOptions.Compiled,
        TimeSpan.FromMilliseconds(50));

    /// <summary>Emits the field-names catalog class source + diagnostics.</summary>
    /// <param name="spec">Parsed d2result-envelope spec.</param>
    /// <returns>Generated source + diagnostics.</returns>
    public static EmitResult Emit(D2ResultEnvelopeSpec spec)
    {
        var diagnostics = ImmutableArray.CreateBuilder<EmitDiagnostic>();
        var validFields = ValidateFields(spec.Fields, diagnostics);
        var source = EmitSource(validFields);
        return new EmitResult(source, diagnostics.ToImmutable());
    }

    private static List<D2ResultEnvelopeFieldEntry> ValidateFields(
        ImmutableArray<D2ResultEnvelopeFieldEntry> fields,
        ImmutableArray<EmitDiagnostic>.Builder diagnostics)
    {
        var valid = new List<D2ResultEnvelopeFieldEntry>();
        var seenConstNames = new HashSet<string>(StringComparer.Ordinal);
        var seenValues = new HashSet<string>(StringComparer.Ordinal);

        foreach (var entry in fields)
        {
            if (!sr_constNameRegex.IsMatch(entry.ConstName))
            {
                diagnostics.Add(EmitDiagnostics.InvalidConstName(entry.ConstName));
                continue;
            }

            if (entry.Value.Falsey())
            {
                diagnostics.Add(EmitDiagnostics.EmptyValue(entry.ConstName));
                continue;
            }

            if (!seenConstNames.Add(entry.ConstName))
            {
                diagnostics.Add(EmitDiagnostics.DuplicateFieldConstName(entry.ConstName));
                continue;
            }

            if (!seenValues.Add(entry.Value))
            {
                diagnostics.Add(EmitDiagnostics.DuplicateFieldValue(entry.Value));
                continue;
            }

            valid.Add(entry);
        }

        return valid;
    }

    private static string EmitSource(List<D2ResultEnvelopeFieldEntry> entries)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// -----------------------------------------------------------------------");
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine(
            "//   Generated by DcsvIo.D2.Result.Envelope.SourceGen.D2ResultEnvelopeGenerator");
        sb.AppendLine(
            "//   from contracts/d2result-envelope/d2result-envelope.spec.json");
        sb.AppendLine("//   (the source of truth). Manual edits will be lost on rebuild.");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine("// -----------------------------------------------------------------------");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine();
        sb.AppendLine($"namespace {ROOT_NAMESPACE};");
        sb.AppendLine();
        sb.AppendLine(
            "/// <summary>Spec-derived JSON property-name catalog for the D2Result Shape B "
                + "wire envelope (the BFF gateway response shape that every frontend reads). "
                + "Every JSON serializer / deserializer references these constants for property "
                + "names instead of inline string literals — drift between the wire and the "
                + "code is structurally impossible.</summary>");
        sb.AppendLine(
            "/// <remarks>Cross-language parity: the SAME spec drives the TS-side "
                + "<c>@dcsv-io/d2-result</c> catalog via <c>tools/ts-codegen</c>. Both sides emit the "
                + "same property names byte-for-byte; cross-language wire drift is "
                + "impossible.</remarks>");
        sb.AppendLine($"public static class {CLASS_NAME}");
        sb.AppendLine("{");

        var first = true;
        foreach (var entry in entries)
        {
            if (!first)
                sb.AppendLine();
            first = false;
            sb.AppendLine($"    /// <summary>{EscapeXmlDoc(entry.Doc)}</summary>");
            sb.AppendLine(
                $"    public const string {entry.ConstName} = "
                    + $"\"{EscapeStringLiteral(entry.Value)}\";");
        }

        if (entries.Count > 0)
            sb.AppendLine();

        EmitAllFields(sb, entries);

        sb.AppendLine("}");
        return sb.ToString().LfNormalized();
    }

    private static void EmitAllFields(StringBuilder sb, List<D2ResultEnvelopeFieldEntry> entries)
    {
        sb.AppendLine(
            "    /// <summary>All declared D2Result envelope field wire-names "
                + "in spec order.</summary>");
        sb.AppendLine(
            "    public static IReadOnlyList<string> AllFields => sr_allFields;");
        sb.AppendLine();
        sb.AppendLine(
            "    private static readonly IReadOnlyList<string> sr_allFields = new string[]");
        sb.AppendLine("    {");
        foreach (var entry in entries)
            sb.AppendLine($"        \"{EscapeStringLiteral(entry.Value)}\",");

        sb.AppendLine("    };");
    }

    private static string EscapeStringLiteral(string value) => value
        .Replace("\\", "\\\\")
        .Replace("\"", "\\\"");

    private static string EscapeXmlDoc(string value) => value
        .Replace("&", "&amp;")
        .Replace("<", "&lt;")
        .Replace(">", "&gt;");
}
