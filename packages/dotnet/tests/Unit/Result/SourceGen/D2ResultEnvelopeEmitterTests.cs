namespace DcsvIo.D2.Tests.Unit.Result.SourceGen;

using System.Collections.Immutable;
using AwesomeAssertions;
using DcsvIo.D2.Result.Envelope.SourceGen;
using Xunit;

/// <summary>
/// Pure-logic tests for the d2result-envelope emitter. Drives the emitter
/// directly with synthetic specs and asserts both the generated source
/// shape (per-VALUE pin) and the diagnostics surfaced for invalid spec
/// inputs.
/// </summary>
public sealed class D2ResultEnvelopeEmitterTests
{
    [Theory]
    [InlineData("SUCCESS", "success")]
    [InlineData("DATA", "data")]
    [InlineData("MESSAGES", "messages")]
    [InlineData("INPUT_ERRORS", "inputErrors")]
    [InlineData("ERROR_CODE", "errorCode")]
    [InlineData("TRACE_ID", "traceId")]
    [InlineData("STATUS_CODE", "statusCode")]
    public void Emit_ValidField_EmitsPublicConstWithExactValue(
        string constName, string wireValue)
    {
        var spec = MakeSpec(new D2ResultEnvelopeFieldEntry(constName, wireValue, "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().BeEmpty();
        result.GeneratedSource.Should().Contain(
            $"public const string {constName} = \"{wireValue}\";");
    }

    [Fact]
    public void Emit_ValidSpec_EmitsHeaderAndNamespaceAndClass()
    {
        var spec = MakeSpec(new D2ResultEnvelopeFieldEntry("SUCCESS", "success", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().BeEmpty();
        result.GeneratedSource.Should().Contain("// <auto-generated>");
        result.GeneratedSource.Should().Contain("namespace DcsvIo.D2.Result;");
        result.GeneratedSource.Should().Contain(
            "public static class D2ResultEnvelopeFieldNames");
        result.GeneratedSource.Should().Contain("#nullable enable");
    }

    [Fact]
    public void Emit_DuplicateConstName_EmitsD2DRE002Diagnostic()
    {
        var spec = MakeSpec(
            new D2ResultEnvelopeFieldEntry("SUCCESS", "success", "Doc."),
            new D2ResultEnvelopeFieldEntry("SUCCESS", "ok", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().ContainSingle(d =>
            d.DescriptorId == DiagnosticIds.DuplicateFieldConstName);
    }

    [Fact]
    public void Emit_DuplicateValue_EmitsD2DRE003Diagnostic()
    {
        var spec = MakeSpec(
            new D2ResultEnvelopeFieldEntry("SUCCESS_A", "success", "Doc."),
            new D2ResultEnvelopeFieldEntry("SUCCESS_B", "success", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().ContainSingle(d =>
            d.DescriptorId == DiagnosticIds.DuplicateFieldValue);
    }

    [Theory]
    [InlineData("lowerCase")]
    [InlineData("123_starts_with_digit")]
    [InlineData("Has-Hyphen")]
    [InlineData("")]
    [InlineData("hasSpaces inside")]
    public void Emit_InvalidConstName_EmitsD2DRE004Diagnostic(string badConstName)
    {
        var spec = MakeSpec(new D2ResultEnvelopeFieldEntry(badConstName, "value", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().ContainSingle(d =>
            d.DescriptorId == DiagnosticIds.InvalidConstName);
    }

    [Theory]
    [InlineData("")]
    [InlineData("   ")]
    [InlineData("\t")]
    public void Emit_EmptyOrWhitespaceValue_EmitsD2DRE005Diagnostic(string emptyValue)
    {
        var spec = MakeSpec(new D2ResultEnvelopeFieldEntry("SUCCESS", emptyValue, "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().ContainSingle(d =>
            d.DescriptorId == DiagnosticIds.EmptyValue);
    }

    [Fact]
    public void Emit_PreservesSpecOrderOfFields()
    {
        // Spec ordering drives emitted ordering — predictable diff on
        // spec edits.
        var spec = MakeSpec(
            new D2ResultEnvelopeFieldEntry("Z", "z_val", "Doc."),
            new D2ResultEnvelopeFieldEntry("A", "a_val", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().BeEmpty();
        var zPos = result.GeneratedSource.IndexOf(
            "Z = \"z_val\"", System.StringComparison.Ordinal);
        var aPos = result.GeneratedSource.IndexOf(
            "A = \"a_val\"", System.StringComparison.Ordinal);
        zPos.Should().BeLessThan(aPos);
    }

    [Fact]
    public void Emit_AllFieldsArray_ContainsEveryWireValueInOrder()
    {
        var spec = MakeSpec(
            new D2ResultEnvelopeFieldEntry("SUCCESS", "success", "Doc."),
            new D2ResultEnvelopeFieldEntry("DATA", "data", "Doc."),
            new D2ResultEnvelopeFieldEntry("STATUS_CODE", "statusCode", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().BeEmpty();
        result.GeneratedSource.Should().Contain("public static IReadOnlyList<string> AllFields");
        var successPos = result.GeneratedSource.IndexOf(
            "\"success\",", System.StringComparison.Ordinal);
        var dataPos = result.GeneratedSource.IndexOf(
            "\"data\",", System.StringComparison.Ordinal);
        var statusPos = result.GeneratedSource.IndexOf(
            "\"statusCode\",", System.StringComparison.Ordinal);

        // Spec order preserved in the AllFields literal.
        successPos.Should().BeGreaterThan(0);
        dataPos.Should().BeGreaterThan(successPos);
        statusPos.Should().BeGreaterThan(dataPos);
    }

    [Fact]
    public void Emit_RunsTwiceWithIdenticalInput_ProducesIdenticalSource()
    {
        var spec = MakeSpec(new D2ResultEnvelopeFieldEntry("SUCCESS", "success", "Doc."));

        var first = D2ResultEnvelopeEmitter.Emit(spec);
        var second = D2ResultEnvelopeEmitter.Emit(spec);

        second.GeneratedSource.Should().Be(first.GeneratedSource);
    }

    [Fact]
    public void Emit_EscapesXmlDocSpecialCharacters()
    {
        // Doc text with <, >, & must be XML-encoded in the emitted summary
        // otherwise the generated source won't compile.
        var spec = MakeSpec(
            new D2ResultEnvelopeFieldEntry(
                "DATA",
                "data",
                "Record<string, unknown> & generics."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().BeEmpty();
        result.GeneratedSource.Should().Contain("&lt;string, unknown&gt;");
        result.GeneratedSource.Should().Contain("&amp; generics");
    }

    [Fact]
    public void Emit_EscapesStringLiteralBackslashAndQuote()
    {
        var spec = MakeSpec(
            new D2ResultEnvelopeFieldEntry("WEIRD", @"a\b""c", "Doc."));

        var result = D2ResultEnvelopeEmitter.Emit(spec);

        result.Diagnostics.Should().BeEmpty();
        result.GeneratedSource.Should().Contain(@"public const string WEIRD = ""a\\b\""c""");
    }

    private static D2ResultEnvelopeSpec MakeSpec(params D2ResultEnvelopeFieldEntry[] fields) =>
        new(fields.ToImmutableArray());
}
