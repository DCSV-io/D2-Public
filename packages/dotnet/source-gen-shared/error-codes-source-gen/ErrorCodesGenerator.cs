namespace DcsvIo.D2.ResultErrorCodes.SourceGen;

using System;
using DcsvIo.D2.ErrorCodes.SourceGen;
using Microsoft.CodeAnalysis;

/// <summary>
/// Roslyn incremental source generator that emits the <c>ErrorCodes</c>
/// constants class into <c>DcsvIo.D2.Result</c> by reading
/// <c>contracts/error-codes/error-codes.spec.json</c> via
/// <c>AdditionalFiles</c>. A thin shell over the shared
/// <see cref="ErrorCodesEngine"/> — it owns only the generic catalog's
/// identity (assembly name + this type's FQN, both load-bearing for the
/// on-disk generated path) + its <see cref="CatalogConfig"/>; all generation
/// logic lives in the shared engine. Single-target — only emits when the
/// consuming assembly is <c>DcsvIo.D2.Result</c>.
/// </summary>
[Generator]
public sealed class ErrorCodesGenerator : IIncrementalGenerator
{
    private const string _CONSTANTS_BANNER =
        "// -----------------------------------------------------------------------\n" +
        "// <auto-generated>\n" +
        "//   Generated by DcsvIo.D2.Result.ErrorCodes.SourceGen\n" +
        "//   (ErrorCodesGenerator) from contracts/error-codes/error-codes.spec.json\n" +
        "//   (the source of truth). Manual edits will be lost on rebuild.\n" +
        "// </auto-generated>\n" +
        "// -----------------------------------------------------------------------\n";

    private const string _CONSTANTS_SUMMARY =
        "/// <summary>\n" +
        "/// Standardized error codes used across the application as the value of\n" +
        "/// <see cref=\"D2Result.ErrorCode\"/>. Consumers may compare against these\n" +
        "/// constants (or use the per-code boolean helpers on <see cref=\"D2Result\"/>)\n" +
        "/// to discriminate between known failure modes.\n" +
        "/// </summary>\n" +
        "/// <remarks>\n" +
        "/// Generated from the spec file - cross-language drift between the .NET\n" +
        "/// constants and the TS-side @dcsv-io/d2-result catalog is structurally impossible\n" +
        "/// (the TS emitter consumes the same spec via tools/ts-codegen).\n" +
        "/// </remarks>\n";

    private const string _ALL_CODES_DOC =
        "    /// <summary>\n" +
        "    /// All declared error codes in spec order. Useful for telemetry\n" +
        "    /// tag-value membership checks and cross-spec consistency tests.\n" +
        "    /// </summary>\n";

    private const string _GET_HTTP_STATUS_DOC =
        "    /// <summary>\n" +
        "    /// Returns the HTTP status code declared in the spec for\n" +
        "    /// <paramref name=\"errorCode\"/>, or <c>500</c> if the code is not\n" +
        "    /// recognized (defensive default - every shipped code is in the table).\n" +
        "    /// </summary>\n" +
        "    /// <param name=\"errorCode\">The error code.</param>\n" +
        "    /// <returns>The HTTP status code.</returns>\n";

    private const string _FACTORIES_BANNER =
        "// -----------------------------------------------------------------------\n" +
        "// <auto-generated>\n" +
        "//   Generated by DcsvIo.D2.Result.ErrorCodes.SourceGen\n" +
        "//   (ErrorCodesGenerator) from contracts/error-codes/error-codes.spec.json\n" +
        "//   (the source of truth). Manual edits will be lost on rebuild.\n" +
        "// </auto-generated>\n" +
        "// -----------------------------------------------------------------------\n";

    private const string _FACTORIES_SUMMARY =
        "/// <summary>\n" +
        "/// Generated semantic failure factories on <see cref=\"D2Result\"/>. Each\n" +
        "/// factory constructs the failure with the spec-declared HTTP status,\n" +
        "/// error code, and default <see cref=\"TKMessage\"/>. Prefer a semantic\n" +
        "/// factory over the raw <see cref=\"Fail\"/> when one matches the failure\n" +
        "/// mode. The hand-rolled <c>Ok</c>/<c>Created</c>/<c>Fail</c>/<c>SomeFound</c>\n" +
        "/// factories live on the same partial class.\n" +
        "/// </summary>\n";

    private const string _GENERIC_FACTORIES_SUMMARY =
        "/// <summary>\n" +
        "/// Generated semantic failure factories on <see cref=\"D2Result{TData}\"/> -\n" +
        "/// the typed twins of the non-generic factories, carrying <c>default</c>\n" +
        "/// data. The hand-rolled <c>Ok</c>/<c>Created</c>/<c>Fail</c>/<c>Bubble</c>/\n" +
        "/// <c>BubbleFail</c>/<c>SomeFound</c>/<c>PartialSuccess</c> factories live on\n" +
        "/// the same partial class.\n" +
        "/// </summary>\n";

    private const string _BOOLEANS_SUMMARY =
        "/// <summary>\n" +
        "/// Generated per-error-code boolean discriminators on <see cref=\"D2Result\"/>.\n" +
        "/// Prefer these over manual <c>ErrorCode == ErrorCodes.X</c> comparisons - they\n" +
        "/// read better at the call site. The composite (<c>IsPartialOrMissing</c>/\n" +
        "/// <c>IsTransientRetryable</c>) and status-based (<c>IsOk</c>/<c>IsCreated</c>)\n" +
        "/// discriminators stay hand-rolled on the same partial class.\n" +
        "/// </summary>\n" +
        "/// <remarks>\n" +
        "/// All discriminators carry <see cref=\"System.Text.Json.Serialization.JsonIgnoreAttribute\"/>\n" +
        "/// - they are in-process call-site helpers, not part of the wire envelope.\n" +
        "/// </remarks>\n";

    private static readonly CatalogConfig sr_config = new(
        TargetAssemblyName: "DcsvIo.D2.Result",
        SpecFileName: "error-codes.spec.json",
        RootNamespace: "DcsvIo.D2.Result",
        ConstantsClassName: "ErrorCodes",
        ConstantsSourceName: "ErrorCodes.g.cs",
        ConstantsBanner: _CONSTANTS_BANNER,
        ConstantsSummary: _CONSTANTS_SUMMARY,
        AllCodesDoc: _ALL_CODES_DOC,
        GetHttpStatusDoc: _GET_HTTP_STATUS_DOC,
        DomainPrefix: null,
        EmitKebabCase: false,
        EmitFailures: true,
        FailuresClassName: null,
        FailuresSourceName: null,
        FailuresBanner: null,
        FailuresSummary: null,
        ValidateCategory: false,
        MalformedSpecId: DiagnosticIds.MalformedSpec,
        DuplicateCodeId: DiagnosticIds.DuplicateCode,
        InvalidHttpStatusId: DiagnosticIds.InvalidHttpStatus,
        InvalidCodeId: DiagnosticIds.InvalidCode,
        MissingDocId: DiagnosticIds.MissingDoc,
        UnknownCategoryId: null,
        DuplicateFactoryNameId: null,
        FactoryHost: FactoryHost.Base,
        BaseHostType: "D2Result",
        BaseGenericHostType: "D2Result<TData>",
        BaseFactoriesSourceName: "D2Result.Factories.g.cs",
        BaseFactoriesBanner: _FACTORIES_BANNER,
        BaseFactoriesSummary: _FACTORIES_SUMMARY,
        BaseGenericFactoriesSourceName: "D2Result.Generic.Factories.g.cs",
        BaseGenericFactoriesBanner: _FACTORIES_BANNER,
        BaseGenericFactoriesSummary: _GENERIC_FACTORIES_SUMMARY,
        BaseBooleansSourceName: "D2Result.Booleans.g.cs",
        BaseBooleansBanner: _FACTORIES_BANNER,
        BaseBooleansSummary: _BOOLEANS_SUMMARY);

    /// <summary>
    /// Gets the generic catalog configuration. Exposed for unit tests that
    /// drive the shared engine emitters directly with the real catalog config.
    /// </summary>
    internal static CatalogConfig Config => sr_config;

    /// <inheritdoc/>
    public void Initialize(IncrementalGeneratorInitializationContext context) =>
        ErrorCodesEngine.Run(context, sr_config, ResolveDescriptor);

    private static DiagnosticDescriptor ResolveDescriptor(string id) => id switch
    {
        DiagnosticIds.MalformedSpec => DiagnosticDescriptors.MalformedSpec,
        DiagnosticIds.DuplicateCode => DiagnosticDescriptors.DuplicateCode,
        DiagnosticIds.InvalidHttpStatus => DiagnosticDescriptors.InvalidHttpStatus,
        DiagnosticIds.InvalidCode => DiagnosticDescriptors.InvalidCode,
        DiagnosticIds.MissingDoc => DiagnosticDescriptors.MissingDoc,
        _ => throw new InvalidOperationException(
            $"Unknown EmitDiagnostic descriptor id '{id}'."),
    };
}
